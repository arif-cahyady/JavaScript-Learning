// if Statment
// Sebuah alur program unutk mengakomodasi dan mengecek sebuah kondisi

let a = 10
let b = "20"

if (a < b) {
    console.log("Benar")
}

// Else Statement
// digunakan unutk mengembalikan suatu nilai  apabila kondisi if statement bernilai false

if (a > b) {
    console.log("Benar")
} else {
    console.log("Salah")
}

// Else if statement
// digunakan untuk mengecek lebih dari satu kondisi diadalam if statement
if (a === b) {
    console.log("Identik")
} else if (a == b) {
    console.log("Nilainya Sama")
} else if (a < b) {
    console.log("Benar")
}else {
    console.log("Salah")
}