/*
Manual
    Proses pengujian secara manual oleh seorang yang ditunjuk sebagai test, atau sebagian pengguna 
    yang memang mendapatkan akses untuk pengujian pre-release. Proses ini biasanya berkaitan dengan 
    usability, accessibility dari sebuah aplikasi.

Otomatis
    Proses pengujian secara otomatis dilakukan oleh komputer dengan menuliskan script khusus, 
    biasanya dilakukan oleh software engineer langsung ataupun oleh seorang QA Engineer. 
    Proses ini biasanya berkaitan dengan fungsionalitas dari sebuah aplikasi.

    Dari proses pengujian di atas, jenis-jenis pengujian dalam pengembangan perangkat lunak secara umum dapat dibagi menjadi 4 jenis pengujian, yaitu:

Static test
    Memastikan tidak adanya typo (naming convention yang standar) dan memastikan
    tidak ada error types.

Unit test
    Dilakukan untuk memastikan bahwa setiap unit kode yang kita tulis sudah bisa
    bekerja sesuai harapan. Unit sendiri berarti komponen terkecil yang dapat diuji secara 
    terisolasi dalam perangkat lunak yang kita buat, dapat berupa fungsi bahkan kelas jika kita 
    menggunakan paradigma OOP. Proses ini juga dapat diautomasikan.

Integration test
    Memastikan beberapa serangkaian fungsi yang saling ketergantungan satu sama lain 
    berjalan semestinya. Proses pengujian ini dapat dilakukan secara otomatis dengan 
    menuliskan script test.

End-to-End test
    Proses pengujian sebuah aplikasi untuk menguji flow dari awal hingga akhir, 
    layaknya seorang user saat menggunakan aplikasi. Memastikan bahwasanya aplikasi berfungsi 
    selayaknya. Biasanya proses ini dapat dilakukan secara otomatis maupun manual oleh tester
*/